// Function to find square root
// x: element to find square root
class Solution{
  public:
    long long int floorSqrt(long long int x) 
    {
        // Your code goes here  
        long long i =1;
        long long num = x;
        long long cnt =0;
        
        while (num>=i){
            num = num -i;
            i+=2;
            cnt++;
        }
        return cnt;
    }
};

